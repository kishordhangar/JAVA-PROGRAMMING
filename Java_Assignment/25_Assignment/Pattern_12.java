package Project;

public class Pattern_12 
{
	public static void main(String args[])
	{
	int col ; 
    int row ; 
    for(row = 0 ; row < 5 ; row ++) {  //rows
    
        for(col = 0 ; col < 5 ; col ++)
        {  //col
           
            	System.out.print(+col+row);              
        } 
        System.out.print("\n");
    }
}

}
